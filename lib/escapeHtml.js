/**
 * Escapes user-supplied strings before embedding in HTML email bodies.
 * Prevents XSS / HTML injection in emails sent to the business inbox.
 */
export function escapeHtml(str) {
  if (typeof str !== 'string') return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}
